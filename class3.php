A paragraph may be well described as a group of several well organized,
 complete, correct, and concise sentences combined together focusing on one subject,
 theme, or central idea. 
 It can be an independent short composition written on certain central idea or it may also be a part of lengthy composition 
 like essay, story or any kind of prose/poetry work. 
 In lengthy composition paragraphs are made to make the  long write-ups easily comprehendible.
 As far as length of a paragraph is concerned there is no set rule for it. They may be from one to many lines long.
